-- Same problem as unit_price/subtotal (see 20260713020000), one step
-- removed: shipping_fee is computed client-side in src/lib/shipping.ts
-- from a per-city rate table that only ever existed in the frontend, then
-- sent straight to the orders insert with no server-side check at all.
--
-- This mirrors that rate table into Postgres and overwrites shipping_fee
-- from it (by customer_city) the moment an order is inserted, using the
-- same two-tier lookup as the frontend: exact normalized city match first,
-- then a "base name" match (drops parenthetical/region qualifiers, e.g.
-- "Afsou ( nador )" -> "afsou"), falling back to the standard default fee
-- if nothing matches. normalized_city/base_city are precomputed here in
-- the same way src/lib/shipping.ts's normalize()/baseName() do, so the two
-- stay in lockstep; if the rate table or that normalization logic ever
-- changes, both sides need updating together.

CREATE EXTENSION IF NOT EXISTS unaccent WITH SCHEMA extensions;

CREATE TABLE public.shipping_rates (
  id serial PRIMARY KEY,
  city text NOT NULL,
  fee numeric(10,2) NOT NULL CHECK (fee >= 0),
  normalized_city text NOT NULL,
  base_city text NOT NULL
);

-- Lookups happen by normalized_city / base_city (see functions below);
-- these indexes keep the trigger on every order insert cheap. Not UNIQUE:
-- the source rate sheet has legitimate duplicate/overlapping labels, and
-- "first occurrence wins" (id ASC) reproduces the frontend's Map behavior.
CREATE INDEX shipping_rates_normalized_city_idx ON public.shipping_rates (normalized_city, id);
CREATE INDEX shipping_rates_base_city_idx ON public.shipping_rates (base_city, id);

ALTER TABLE public.shipping_rates ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Anyone reads shipping rates" ON public.shipping_rates FOR SELECT USING (true);

