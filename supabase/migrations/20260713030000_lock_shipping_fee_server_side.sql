-- Mirrors src/lib/shipping.ts's normalize(): strip accents, lowercase,
-- drop parenthetical qualifiers, drop a trailing quoted region label,
-- collapse remaining punctuation/whitespace to single spaces, trim.
CREATE OR REPLACE FUNCTION public._shipping_normalize(raw text)
RETURNS text LANGUAGE sql STABLE AS $$
  SELECT trim(
    regexp_replace(
      regexp_replace(
        regexp_replace(lower(extensions.unaccent(coalesce(raw, ''))), '\([^)]*\)', ' ', 'g'),
        '[''"’][^''"’]*$', ' ', 'g'
      ),
    '[^a-z0-9]+', ' ', 'g')
  )
$$;

-- Mirrors src/lib/shipping.ts's baseName(): the normalized name, truncated
-- at the first standalone occurrence of the word "region".
CREATE OR REPLACE FUNCTION public._shipping_base_name(raw text)
RETURNS text LANGUAGE sql STABLE AS $$
  SELECT trim(regexp_replace(public._shipping_normalize(raw), '\yregion\y.*$', ''))
$$;

-- Mirrors src/lib/shipping.ts's getShippingFee(): exact normalized match
-- first, then base-name match, then the standard default fee. "First
-- occurrence wins" ties are resolved the same way the frontend's Map does,
-- via id ASC (insertion order == the source rate sheet's order).
CREATE OR REPLACE FUNCTION public.get_shipping_fee(city_input text)
RETURNS numeric(10,2) LANGUAGE plpgsql STABLE AS $$
DECLARE
  full_norm text := public._shipping_normalize(city_input);
  base_norm text;
  matched_fee numeric(10,2);
BEGIN
  IF city_input IS NULL OR trim(city_input) = '' THEN
    RETURN 45; -- DEFAULT_SHIPPING_FEE, see src/data/shippingRates.ts
  END IF;

  SELECT fee INTO matched_fee FROM public.shipping_rates
  WHERE normalized_city = full_norm ORDER BY id ASC LIMIT 1;
  IF FOUND THEN RETURN matched_fee; END IF;

  base_norm := public._shipping_base_name(city_input);
  SELECT fee INTO matched_fee FROM public.shipping_rates
  WHERE base_city = base_norm ORDER BY id ASC LIMIT 1;
  IF FOUND THEN RETURN matched_fee; END IF;

  RETURN 45; -- DEFAULT_SHIPPING_FEE
END;
$$;

CREATE OR REPLACE FUNCTION public.set_order_shipping_fee()
RETURNS trigger LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
BEGIN
  NEW.shipping_fee := public.get_shipping_fee(NEW.customer_city);
  RETURN NEW;
END;
$$;

CREATE TRIGGER orders_set_shipping_fee
  BEFORE INSERT ON public.orders
  FOR EACH ROW EXECUTE FUNCTION public.set_order_shipping_fee();
